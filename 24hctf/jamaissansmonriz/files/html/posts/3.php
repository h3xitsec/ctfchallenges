                        <p>Le riz complet, appelé aussi riz brun ou riz cargo, est un riz décortiqué, c'est-à-dire que le riz brut (paddy) a été débarrassé de sa balle, ses enveloppes extérieures ou glumelles adhérentes non comestibles. Contrairement au riz blanc, son son et son germe sont toujours présents, et sa couleur est beige, rouge ou noire.</p>
                        
                        <h2 class="section-heading">Description</h2>
                        <p>Il est constitué par le grain entier qui a conservé une pellicule qui constitue le son de riz (formé du péricarpe du fruit et du tégument de la graine intimement soudés pour former le caryopse), ainsi que le germe. C'est un type de grain entier. Il est plus dur à mastiquer que le riz blanc et rancit plus rapidement, mais est plus digeste et a plus de valeur nutritive, car le son de riz est riche en différents nutriments : fibres, vitamines B1 et B6, fer, magnésium, potassium, tandis que le germe apporte des acides gras et des protéines et que la couche à aleurone apporte des éléments qui inhibent l'angiotensine ayant une influence dans le développement de l'hypertension artérielle et l'athérosclérose.</p>
                        <p>Sa cuisson est facilitée par un trempage préalable.</p>

                        <h2 class="section-heading">Symbolique et représentations</h2>
                        <p>En Asie (sauf au Japon), il est associé à la pauvreté et aux temps de guerre. Au Japon et en Occident, il est associé à une alimentation saine du fait de la place très importante qu'il tient dans la cuisine macrobiotique.</p>
                        <a href="#!"><img class="img-fluid" src="https://upload.wikimedia.org/wikipedia/commons/d/d1/Reis_-_Sorte_C_voll.jpg" alt="..." /></a>
                        
                        <h2 class="section-heading">Appellation « riz cargo »</h2>
                        <p>Le riz était autrefois transporté d'Asie en Europe sous forme de riz décortiqué car il se conserve sous cette forme trois fois plus longtemps que sous la forme de riz blanchi, d'où le nom de « riz cargo » ou « riz de cargaison ». Certes, le riz « paddy » se conserve encore plus longtemps, mais le riz « cargo » est mieux adapté au transport car son volume après décorticage est réduit de 40 à 50 % et son poids de 20 à 30 %.</p>
                        
